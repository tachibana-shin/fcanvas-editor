<template>
  <div class="flex items-center mb-1.5 select-none">
    <q-icon v-if="dir" icon="mdi-chevron-right" />

    <div className="absolute top-0 left-0 w-full h-full bg-[rgba(0,0,0,.5)]" />

    <div
      className="flex items-center relative z-10"
      :class="{
        'ml-20px': dir
      }"
    >
      <img
        className="w-[1.2rem] h-[1.2rem]"
        :src="
          getIcon({
            light: false,
            isFolder: dir,
            isOpen: false,
            filepath: inputName
          })
        "
      />

      <InputAutoFocus
        v-model="inputName"
        @blur="onBlur"
        @keydown="onKeydown"
        className="bg-transparent text-[14px] ml-2 truncate w-full border focus-visible:outline-none"
        :class="
          errorFileName
            ? errorFileName.type === 'warn'
              ? ' border-yellow-600'
              : ' border-red-600'
            : ' border-cyan-600'
        "
      />

      <div
        v-if="errorFileName"
        className="absolute pl-[1.7rem] w-full top-[100%]"
      >
        <span
          className="text-[12px] px-1 py-[5px] border border-top-none block w-full"
          :class="
            errorFileName.type === 'warn'
              ? ' bg-yellow-900 border-yellow-600'
              : ' bg-red-900 border-red-600'
          "
        >
          {errorFileName.message}
        </span>
      </div>
    </div>
  </div>
</template>

<script lang="ts" setup>
import { computed, ref, watch } from "vue"
import { checkErrorFileName } from "./checkErrorFileName"

import getIcon from "src/assets/extensions/material-icon-theme/dist/getIcon"

const props = defineProps<{
  dir: boolean
  defaultValue?: string
  siblings?: string[]
}>()
const emit = defineEmits<{
  (name: "save", value: string): void
  (name: "blur"): void
}>()

const inputName = ref("")
const skipEmptyFileName = ref(false)

const watcher = watch(inputName, () => {
  if (inputName.value !== "") {
    skipEmptyFileName.value = true
    watcher()
  }
})

const errorFileName = computed(() =>
  checkErrorFileName(
    inputName.value,
    props.siblings || [],
    skipEmptyFileName.value
  )
)

function onBlur() {
  if (
    !errorFileName.value &&
    inputName.value !== "" &&
    inputName.value !== props.defaultValue
  ) {
    emit("save", inputName.value)
  }

  emit("blur")
}

function onKeydown(event: KeyboardEvent) {
  if (event.key === "Enter") {
    if (!errorFileName.value && inputName.value !== "") {
      ;(event.target as HTMLInputElement).blur()
    }
  }
}
</script>
